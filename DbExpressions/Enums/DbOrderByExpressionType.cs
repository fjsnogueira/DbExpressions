namespace DbExpressions
{
    /// <summary>
    /// Describes the possible sort directions for an <see cref="DbOrderByExpression"/>.
    /// </summary>
    public enum DbOrderByExpressionType
    {
        /// <summary>
        /// Sorts the element in an ascending order.
        /// </summary>
        Ascending,

        /// <summary>
        /// Sorts the element in an descending order.
        /// </summary>
        Descending
    }
}
